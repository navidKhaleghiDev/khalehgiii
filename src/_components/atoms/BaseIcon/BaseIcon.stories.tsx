/**
 * This function uses the custom IconListStorybook component to display a consolidated list of icons.
 * @param args {color, size, hoverColor, className}
 * @returns
 */
export function ListIcon({
  color,
  size,
  hoverColor,
  className,
}: Omit<IconListStorybookProps, 'icons'>) {
  const iconValue = Object.values(iconOptions);
  const iconValueDuotone = Object.values(iconDuotoneOptions);
  const iconValueML = Object.values(iconMaterialLineOptions);
  const iconKey = Object.keys(iconOptions);
  const iconKeyDuotone = Object.keys(iconDuotoneOptions);
  const iconKeyML = Object.keys(iconMaterialLineOptions);
  return (
    <>
      <Typography
        variant="body3"
        weight="bold"
        className="pb-6 mb-2 border-b border-b-gray-300"
      >
        Phosphore - regular
      </Typography>
      <IconListStorybook
        icons={iconValue}
        color={color}
        size={size}
        hoverColor={hoverColor}
        className={className}
        title={iconKey}
      />

      <Typography
        variant="body3"
        weight="bold"
        className="pb-6 mb-2 border-b border-b-gray-300"
      >
        Phosphore - duotone
      </Typography>
      <IconListStorybook
        icons={iconValueDuotone}
        color={color}
        size={size}
        hoverColor={hoverColor}
        className={className}
        title={iconKeyDuotone}
      />

      <Typography
        variant="body3"
        weight="bold"
        className="pb-6 mb-2 border-b border-b-gray-300"
      >
        Material Line Icons
      </Typography>
      <IconListStorybook
        icons={iconValueML}
        color={color}
        size={size}
        hoverColor={hoverColor}
        className={className}
        title={iconKeyML}
      />
    </>
  );
}

ListIcon.args = {
  color: 'neutral',
  size: 'md',
  hoverColor: 'default',
  className: '',
};
